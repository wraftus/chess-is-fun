class BoardWrapper(object):
    def __init__(self):
        self.board = chess.Board()

    def draw(self):
        p_idx = 0
        filled_board = []
        for c in blank_board:
            if c == '%':
                piece = self.board.piece_at(p_idx)
                filled_board.append(' ' if piece == None else piece.symbol())
                p_idx += 1
            else:
                filled_board.append(c)    
        
        print(''.join(filled_board))
    
    def move(self, from_coord, to_coord, colour):
        move = chess.Move(from_coord, to_coord)
        if not move in self.board.legal_moves:
            return False
        self.board.push(move)
        return True

    def playing(self):
        return not self.board.is_game_over()

    
